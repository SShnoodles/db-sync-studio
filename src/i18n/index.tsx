export type MessageKey = keyof typeof messages.en;

type I18nContextValue = {
  language: Language;
  antdLocale: Locale;
  setLanguage: (language: Language) => void;
  t: (key: MessageKey, values?: Record<string, string | number>) => string;
};

const I18nContext = createContext<I18nContextValue | null>(null);

function initialLanguage(): Language {
  const saved = localStorage.getItem("db-sync-studio.language");
  if (saved === "en" || saved === "zh-CN") return saved;
  return navigator.language.toLowerCase().startsWith("zh") ? "zh-CN" : "en";
}

export function I18nProvider({ children }: { children: ReactNode }) {
  const [language, setLanguageState] = useState<Language>(initialLanguage);
  const value = useMemo<I18nContextValue>(() => {
    const t = (key: MessageKey, values?: Record<string, string | number>) => {
      let message = String(messages[language][key] || messages.en[key] || key);
      if (values) {
        Object.entries(values).forEach(([name, value]) => {
          message = message.split(`{{${name}}}`).join(String(value));
        });
      }
      return message;
    };

    return {
      language,
      antdLocale: language === "zh-CN" ? zhCN : enUS,
      setLanguage: (nextLanguage) => {
        localStorage.setItem("db-sync-studio.language", nextLanguage);
        setLanguageState(nextLanguage);
      },
      t,
    };
  }, [language]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const value = useContext(I18nContext);
  if (!value) throw new Error("useI18n must be used inside I18nProvider");
  return value;
}
